float		perlin_coef(float x, float y, float z)
{
	t_vec3d		unit_cube;
	t_vec3d		fade_curves;
	t_perlin	perlin;

	unit_cube = (t_vec3d){(int)floor(x) & 255, (int)floor(y) & 255,
	(int)floor(z) & 255, 0};
	x -= floor(x);
	y -= floor(y);
	z -= floor(z);
	fade_curves = (t_vec3d){fade(x), fade(y), fade(z), 0};
	perlin.a = g_p[(int)unit_cube.x] + (int)unit_cube.y;
	perlin.aa = g_p[perlin.a] + (int)unit_cube.z;
	perlin.ab = g_p[perlin.a + 1] + (int)unit_cube.z;
	perlin.b = g_p[(int)unit_cube.x + 1] + (int)unit_cube.y;
	perlin.ba = g_p[perlin.b] + (int)unit_cube.z;
	perlin.bb = g_p[perlin.b + 1] + (int)unit_cube.z;
	return (lerp(fade_curves.z, lerp(fade_curves.y, lerp(fade_curves.x,
			grad(g_p[perlin.aa], x, y, z), grad(g_p[perlin.ba], x - 1, y, z)),
			lerp(fade_curves.x, grad(g_p[perlin.ab], x, y - 1, z),
			grad(g_p[perlin.bb], x - 1, y - 1, z))), lerp(fade_curves.y,
			lerp(fade_curves.x, grad(g_p[perlin.aa + 1], x, y, z - 1),
			grad(g_p[perlin.ba + 1], x - 1, y, z - 1)), lerp(fade_curves.x,
			grad(g_p[perlin.ab + 1], x, y - 1, z - 1),
			grad(g_p[perlin.bb + 1], x - 1, y - 1, z - 1)))));
}
