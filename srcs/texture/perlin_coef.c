#include "rt.h"

float		fade(float t)
{
	return (t * t * t * (t * (t * 6 - 15) + 10));
}

float		lerp(float t, float a, float b)
{
	return (a + t * (b - a));
}

float		grad(int hash, float x, float y, float z)
{
	int		h;
	float	u;
	float	v;

	h = hash & 15;
	u = h < 8 || h == 12 || h == 13 ? x : y;
	v = h < 4 || h == 12 || h == 13 ? y : z;
	return (((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v));
}

